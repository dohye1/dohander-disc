export const getResultByScore = (scores: { D: number; i: number; S: number; C: number }): TestResult => {
  const maxScore = Math.max(scores.D, scores.i, scores.S, scores.C);
  
  if (scores.D === maxScore) return results[0];
  if (scores.i === maxScore) return results[1];
  if (scores.S === maxScore) return results[2];
  return results[3];
}; 

